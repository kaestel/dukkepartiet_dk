		<h1>Folketingsreformen</h1>

		<p class="intro">Der er i de senere år gennemført en række omfattende reformer på næsten alle samfundsområder - inklusive en kommunalreform. Dukkepartiet fremsætter nu på baggrund af det dysfunktionelle Folketing og den udbredte politikerlede et forslag til en Folketingsreform, som skal få demokratiet tilbage på sporet.</p>
		<p>I lyset af at politikerne tilsyneladende bruger meget lidt tid på politik, men til gengæld meget tid på magtkampe, fnidder og spin, foreslår Dukkepartiet:</p>
 
	</div> 

	<ul class="items i:carousel">
		<li class="item">
			<p>En reduktion af mandaterne i Folketinget med 20-25 %.</p>	
		</li>
		<li class="item">
			<p>En timeregistrering, der skal dokumentere politikernes arbejde.</p>
		</li>
		<li class="item">
			<p>Afskaffelse af politiske embedsmænd, særlige rådgivere og spindoktorer, så politikerne skal tænke og tale selv.</p>
		</li>
		<li class="item">
			<p>Indførelse af incitamentsstrukturer der belønner politikerne for at udføre deres egentlige hverv: Klare holdninger, politisk ærlighed og handling efter overbevisning er den adfærd, der skal støttes.</p>
		</li>
		<li class="item">
			<p>Der oprettes en akuttelefon hos ombudsmanden, hvor borgerne kan anmelde tilfælde af at politikerne falder tilbage til dukkesnak.</p>
		</li>
		<li class="item">
			<p>Dukkesnak er: <br>Reference til andre politikere. Herunder brug af floskler som “troværdighed”, “løftebrud” og “hængekøje”.</p>
		</li>
		<li class="item">
			<p>Dukkesnak er: <br>Bevidst uklar tale og forsøg på at manipulere med sproget. F.eks. “at skifte standpunkt” i stedet for “at skifte holdning” eller “give urigtige oplysninger” i stedet for “at lyve”.</p>
		</li>
		<li class="item">
			<p>Dukkesnak er: <br>Deltagelse i underholdningsprogrammer eller andre livsstilssammenhænge uden politisk fokus.</p>
		</li>
		<li class="item">
			<p>Finder ombudsmanden at anmeldelsen har et reelt grundlag, gives den skyldige politiker et klip i det retoriske kørekort. Efter tre klip må den pågældende politiker ikke udtale sig til pressen i en periode på tre måneder.</p>
		</li>
	</ul>
		


